    rbt = RedBlackTree()
    for k in [10, 20, 30, 15, 25, 40]:
        rbt.insert(k)

    print("\nRed-Black Tree preorder after inserts:")
    rbt.preorder()

    rbt.delete(20)
    print("\nRed-Black Tree preorder after deleting 20:")
    rbt.preorder()
